var mKey = ""

func init() {
	ex, _ := os.Executable()
	keyFile := filepath.Dir(ex) + "/key"
	b, err := os.ReadFile(keyFile)
	if err != nil {
		return
	}
	mKey = string(b)
	go getState()
}

func main() {
	systray.Run(func() {
		mUrl := systray.AddMenuItem("Open UI", "syncthing home")
		mQuit := systray.AddMenuItem("Quit", "Quit the app")
		for {
			select {
			case <-mQuit.ClickedCh:
				systray.Quit()
			case <-mUrl.ClickedCh:
				_ = open.Run("http://localhost:8384")
			}
		}
	}, nil)
}

func getState() {
	stateUrl := `http://localhost:8384/rest/system/connections`
	syncUrl := `http://localhost:8384/rest/events?since=0&limit=1`
	for {
		time.Sleep(time.Second * 2)
		body, err := httpGET(stateUrl)
		if err != nil {
			continue
		}
		if !strings.Contains(body, `connected": true`) {
			systray.SetIcon(errIcon)
			continue
		}

		body, err = httpGET(syncUrl)
		if err != nil {
			continue
		}
		if strings.Contains(body, `FolderCompletion`) && !strings.Contains(body, `completion": 100`) {
			systray.SetIcon(succIcon)
			continue
		}
		systray.SetIcon(infoIcon)
	}
}

func httpGET(uri string) (string, error) {
	client := &http.Client{}
	req, err := http.NewRequest("GET", uri, nil)
	if err != nil {
		return "", err
	}
	req.Header.Add("X-API-Key", mKey)

	res, err := client.Do(req)
	if err != nil {
		return "", err
	}
	defer res.Body.Close()

	body, err := io.ReadAll(res.Body)
	if err != nil {
		return "", err
	}
	return string(body), nil
}

